// Vector calculation to determine the closest matching Archetype
export function calculateArchetype(riasec, values, aptitude, eq) {
  // Normalize score lists to relative weights (0 to 1)
  
  // RIASEC Categories: realistic, investigative, artistic, social, enterprising, conventional
  // Values: financial, creative, stability, autonomy
  // Aptitude: spatial, verbal, numerical, abstract
  // EQ: stress, grit, collaboration
  
  // We can calculate matching scores for each archetype based on positive associations:
  
  // 1. investigative_tech_architect
  // High: Investigative, Realistic, Conventional, Creative, Autonomy, Spatial, Abstract, Grit
  let scoreITA = 
    (riasec.investigative * 1.5) + 
    (riasec.realistic * 1.0) + 
    (riasec.conventional * 0.5) +
    (values.creative * 1.2) + 
    (values.autonomy * 1.0) +
    (aptitude.spatial * 1.2) + 
    (aptitude.abstract * 1.2) + 
    (eq.grit * 0.8);

  // 2. empathetic_strategist
  // High: Social, Enterprising, Autonomy, Creative, Verbal, Abstract, Collaboration, Stress
  let scoreES = 
    (riasec.social * 1.5) + 
    (riasec.enterprising * 1.0) +
    (values.autonomy * 0.8) + 
    (values.creative * 0.8) +
    (aptitude.verbal * 1.5) + 
    (aptitude.abstract * 1.0) + 
    (eq.collaboration * 1.5) + 
    (eq.stress * 0.5);

  // 3. creative_industrialist
  // High: Artistic, Enterprising, Creative, Financial, Verbal, Spatial, Autonomy, Stress
  let scoreCI = 
    (riasec.artistic * 1.8) + 
    (riasec.enterprising * 1.2) +
    (values.creative * 1.5) + 
    (values.financial * 1.0) +
    (values.autonomy * 0.8) +
    (aptitude.spatial * 1.2) + 
    (aptitude.verbal * 1.0) + 
    (eq.stress * 0.5);

  // 4. analytical_compliance_expert
  // High: Conventional, Investigative, Stability, Financial, Numerical, Abstract, Grit
  let scoreACE = 
    (riasec.conventional * 1.8) + 
    (riasec.investigative * 1.0) +
    (values.stability * 1.5) + 
    (values.financial * 1.0) +
    (aptitude.numerical * 1.5) + 
    (aptitude.abstract * 1.0) + 
    (eq.grit * 0.8) + 
    (eq.collaboration * 0.5);

  // 5. realistic_scientific_pioneer
  // High: Realistic, Investigative, Autonomy, Stability, Spatial, Numerical, Grit, Stress
  let scoreRSP = 
    (riasec.realistic * 1.5) + 
    (riasec.investigative * 1.2) +
    (values.autonomy * 1.2) + 
    (values.stability * 0.8) +
    (aptitude.spatial * 1.5) + 
    (aptitude.numerical * 1.0) + 
    (eq.grit * 1.0) + 
    (eq.stress * 0.8);

  // 6. collaborative_organizer
  // High: Social, Conventional, Stability, Autonomy, Verbal, Numerical, Collaboration, Grit
  let scoreCO = 
    (riasec.social * 1.2) + 
    (riasec.conventional * 1.5) +
    (values.stability * 1.2) + 
    (values.autonomy * 0.5) +
    (aptitude.verbal * 1.2) + 
    (aptitude.numerical * 1.0) + 
    (eq.collaboration * 1.5) + 
    (eq.grit * 0.8);

  // Return the archetype with the highest matching score
  const matches = [
    { id: "investigative_tech_architect", score: scoreITA },
    { id: "empathetic_strategist", score: scoreES },
    { id: "creative_industrialist", score: scoreCI },
    { id: "analytical_compliance_expert", score: scoreACE },
    { id: "realistic_scientific_pioneer", score: scoreRSP },
    { id: "collaborative_organizer", score: scoreCO }
  ];

  matches.sort((a, b) => b.score - a.score);
  return archetypes[matches[0].id];
}
